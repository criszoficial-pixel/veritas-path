// Get daily verse based on day of year
export function getDailyVerseByDate(date: Date = new Date()): DailyVerseData {
  const startOfYear = new Date(date.getFullYear(), 0, 0);
  const diff = date.getTime() - startOfYear.getTime();
  const dayOfYear = Math.floor(diff / (1000 * 60 * 60 * 24));
  
  const index = dayOfYear % dailyVersesCollection.length;
  return dailyVersesCollection[index];
}

// Get verses by topic
export function getVersesByTopic(topic: string): DailyVerseData[] {
  return dailyVersesCollection.filter(v => v.topic === topic);
}

// Get random verse
export function getRandomVerse(): DailyVerseData {
  const index = Math.floor(Math.random() * dailyVersesCollection.length);
  return dailyVersesCollection[index];
}

// Get all available topics
export function getAllTopics(): string[] {
  const topics = new Set(dailyVersesCollection.map(v => v.topic));
  return Array.from(topics);
}
